package com.stefanini.cursojavabasico.variaveis;

public class OperadoresRelacionais {
	public static void main(String[] args) {
			int a = 10;
			int b=  20;
			
			// ==  | Igual
			// !=   | Diferente
			// <    | Menor
			// >    | Maior
			// <=  | Menor ou igual
			// >=  | Maior ou igual
			
			System.out.println( a == b);
			System.out.println( a !=  b);
			System.out.println(a <b);
			System.out.println(a >b);
			System.out.println(a<=b);
			System.out.println(a >=b);
	}
}

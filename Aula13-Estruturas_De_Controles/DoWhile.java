
public class DoWhile {
	public static void main(String[] args) {
		
		// DO WHILE Vai executar pelo menos uma vez o trecho de código, enquanto o WHILE só vai executar se a condição for atendida.
		int count= 1;
		do {
			System.out.println(count);
			count ++;
		} while (count <100);
				
	}
}
